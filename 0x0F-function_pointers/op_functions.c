#include <stdio.h>
#include "calc.h"

/**
 * op_add - adds numbers
 * @a: first digit
 * @b: 2nd digits
 * Return: Always 0
 */
int op_add(int a, int b)
{
	a + b;
}
/**
 * op_sub - subtracts numbers
 * @a: first digit
 * @b: 2nd digits
 * Return: Always 0
 */
int op_sub(int a, int b)
{
	a - b;
}
/**
 * op_mul - multiplies numbers
 * @a: first digit
 * @b: 2nd digits
 * Return: Always 0
 */
int op_mul(int a, int b)
{
	a * b;
}
/**
 * op_div - divides numbers
 * @a: first digit
 * @b: 2nd digits
 * Return: Always 0
 */
int op_div(int a, int b)
{
	a / b;
}
/**
 * op_mod - gives the remainder of a division
 * @a: first digit
 * @b: 2nd digits
 * Return: Always 0
 */
int op_mod(int a, int b)
{
	a % b;
}
